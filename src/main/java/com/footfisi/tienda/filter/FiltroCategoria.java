package com.footfisi.tienda.filter;

public class FiltroCategoria {
	private int idCategoria;
	private String sMarca;
	private String sGenero;
	private String sTipo;

	private int idTalla;
	private String sTalla;

	public int getIdCategoria() {
		return idCategoria;
	}

	public void setIdCategoria(int idCategoria) {
		this.idCategoria = idCategoria;
	}

	public String getsMarca() {
		return sMarca;
	}

	public void setsMarca(String sMarca) {
		this.sMarca = sMarca;
	}

	public String getsGenero() {
		return sGenero;
	}

	public void setsGenero(String sGenero) {
		this.sGenero = sGenero;
	}

	public String getsTipo() {
		return sTipo;
	}

	public void setsTipo(String sTipo) {
		this.sTipo = sTipo;
	}

	public int getIdTalla() {
		return idTalla;
	}

	public void setIdTalla(int idTalla) {
		this.idTalla = idTalla;
	}

	public String getsTalla() {
		return sTalla;
	}

	public void setsTalla(String sTalla) {
		this.sTalla = sTalla;
	}

}
