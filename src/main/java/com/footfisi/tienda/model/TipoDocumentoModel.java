package com.footfisi.tienda.model;

public class TipoDocumentoModel {
	private String sIdTipoDocumento;
	private String sDescripcionLarga;
	private String sDescripcionCorta;

	public String getsIdTipoDocumento() {
		return sIdTipoDocumento;
	}

	public void setsIdTipoDocumento(String sIdTipoDocumento) {
		this.sIdTipoDocumento = sIdTipoDocumento;
	}

	public String getsDescripcionLarga() {
		return sDescripcionLarga;
	}

	public void setsDescripcionLarga(String sDescripcionLarga) {
		this.sDescripcionLarga = sDescripcionLarga;
	}

	public String getsDescripcionCorta() {
		return sDescripcionCorta;
	}

	public void setsDescripcionCorta(String sDescripcionCorta) {
		this.sDescripcionCorta = sDescripcionCorta;
	}

}
